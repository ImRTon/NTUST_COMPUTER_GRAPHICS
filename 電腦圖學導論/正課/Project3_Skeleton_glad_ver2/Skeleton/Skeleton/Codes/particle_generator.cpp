#include "model.h"
#include "particle_generator.h"

ParticleGenerator::ParticleGenerator(Shader* shader, Texture* texture, unsigned int amount)
    : shader(shader), texture(texture), amount(amount)
{
    this->init();
}

void ParticleGenerator::Update(float dt, const std::pair<glm::vec3, glm::vec3>& object, unsigned int newParticles, glm::vec3 offset)
{
    // add new particles 
    for (unsigned int i = 0; i < newParticles; ++i)
    {
        int unusedParticle = this->firstUnusedParticle();
        this->respawnParticle(this->particles[unusedParticle], object, offset);
    }
    // update all particles
    for (unsigned int i = 0; i < this->amount; ++i)
    {
        Particle& p = this->particles[i];
        p.Life -= dt; // reduce life
        if (p.Life > 0.0f)
        {	// particle is alive, thus update
            p.Position -= p.Velocity * dt;
            p.Color.a -= dt * 2.5f;
        }
    }
}

// render all particles
void ParticleGenerator::Draw()
{
    // use additive blending to give it a 'glow' effect
    glEnable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA, GL_ONE);
    this->shader->Use();
    for (Particle particle : this->particles)
    {
        if (particle.Life > 0.0f)
        {
            glUniform3fv(glGetUniformLocation(shader->Program, "offset"), 1, &particle.Position[0]);
            glUniform4fv(glGetUniformLocation(shader->Program, "color"), 1, &particle.Color[0]);
            //this->shader.SetVector2f("offset", particle.Position);
            //this->shader.SetVector4f("color", particle.Color);
            //this->texture.Bind();
            
            
            texture->bind(0);
            glUniform1i(glGetUniformLocation(shader->Program, "tex"), texture->id);


            glBindVertexArray(this->VAO);
            glDrawArrays(GL_TRIANGLES, 0, 36);
            glBindVertexArray(0);
        }
    }
    // don't forget to reset to default blending mode
    glDisable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
    texture->unbind(0);
}

